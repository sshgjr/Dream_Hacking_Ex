/**
 * 꿈 텍스트를 기반으로 간단한 해시값을 생성합니다.
 */
function simpleHash(text: string): number {
  let hash = 0;
  for (let i = 0; i < text.length; i++) {
    const char = text.charCodeAt(i);
    hash = (hash << 5) - hash + char;
    hash |= 0; // 32bit integer 변환
  }
  return Math.abs(hash);
}

/**
 * 꿈 텍스트에서 키워드를 감지하여 특정 응답을 매핑합니다.
 * 키워드가 없으면 해시 기반으로 응답을 선택합니다.
 */
export function getMockInterpretation(dreamText: string): DreamInterpretation {
  const text = dreamText.toLowerCase();

  // 키워드 기반 매핑 (특정 키워드가 포함되면 해당 인덱스의 응답 반환)
  const keywordMap: [string[], number][] = [
    [["물", "바다", "강", "비", "수영", "홍수", "파도"], 0],
    [["사람", "친구", "가족", "엄마", "아빠", "연인", "사랑"], 1],
    [["하늘", "날다", "비행", "새", "구름", "별", "달", "해"], 2],
    [["돈", "금", "보석", "복권", "부자", "지갑", "은행"], 3],
    [["동물", "개", "고양이", "뱀", "호랑이", "용", "돼지"], 4],
    [["집", "건물", "학교", "회사", "문", "계단", "방"], 5],
  ];

  for (const [keywords, index] of keywordMap) {
    if (keywords.some((keyword) => text.includes(keyword))) {
      return mockResponses[index];
    }
  }

  // 키워드 매칭이 없으면 해시 기반 선택
  const hash = simpleHash(dreamText);
  const index = hash % mockResponses.length;
  return mockResponses[index];
}
